def vec2token(vec: List[np.ndarray], n_params: int = N_PARAMS) -> np.ndarray:
    """
    Convert vec to tokens
    vec: converted plant vector from plant string. (depth, organ, [params])
    2025.01.07 Just cat params without scaler. token scaler will be added after this
    """
    tokens = []
    for x in vec:
        depth_organ = x[0] * 6 + x[1]
        token = np.zeros(n_params + 1)  # padding zeros to match the desired length
        token[0] = depth_organ
        token_params = token[1:]
        params = x[2:]

        if x[1] == 0:
            set_shoot_params(token_params, params)
        elif x[1] == 1:
            set_internode_params(token_params, params)
        elif x[1] == 2:
            set_petiole_params(token_params, params)
        elif x[1] in {3, 4, 5}:
            set_leaf_params(token_params, params)
        else:
            raise ValueError(f"Invalid organ type {x[1]}")

        tokens.append(token)

    return np.array(tokens)

def set_shoot_params(token_params: np.ndarray, params: np.ndarray) -> None:
    token_params[0] = params[0]     # shoot_base_rotation_pitch
    token_params[1] = params[1]     # shoot_base_rotation_yaw
    token_params[2] = params[2]     # shoot_base_rotation_roll
    token_params[3] = params[3]     # plant_age
    token_params[4] = params[4]     # shoot_type

def set_internode_params(token_params: np.ndarray, params: np.ndarray) -> None:
    token_params[5] = params[0]     # internode_length
    token_params[6] = params[1]     # internode_radius
    token_params[7] = params[2]     # internode_pitch
    token_params[8] = params[3]     # phyllotactic angle

def set_petiole_params(token_params: np.ndarray, params: np.ndarray) -> None:
    token_params[9] = params[0]     # petiole_length
    token_params[10] = params[1]    # petiole_radius
    token_params[11] = params[2]    # petiole_pitch
    token_params[12] = params[3]    # petiole_curvature
    token_params[13] = params[4]    # leaflet_scale

def set_leaf_params(token_params: np.ndarray, params: np.ndarray) -> None:
    token_params[14] = params[0]    # leaf_scale
    token_params[15] = params[1]    # leaf pitch
    token_params[16] = params[2]    # leaf yaw
    token_params[17] = params[3]    # leaf roll

def token2vec(tokens: np.ndarray) -> List[np.ndarray]:
    vec = []
    for token in tokens:
        label = token[0]
        token_params = token[1:]
        if label == SOS_token:
            pass
        elif label == EOS_token or label == PAD_token:
            break
        else:
            i = label // 6
            j = label % 6
            params = np.zeros(6)
            if j == 0:
                get_shoot_params(params, token_params)
            elif j == 1:
                get_internode_params(params, token_params)
            elif j == 2:
                get_petiole_params(params, token_params)
            elif j in {3, 4, 5}:
                get_leaf_params(params, token_params)
            else:
                raise ValueError(f"Invalid organ type {j}")

            vec.append(np.concatenate(([i, j], params), axis=0))
    return vec

def get_shoot_params(params: np.ndarray, token_params: np.ndarray) -> None:
    params[0] = token_params[0]     # shoot_base_rotation_pitch
    params[1] = token_params[1]     # shoot_base_rotation_yaw
    params[2] = token_params[2]     # shoot_base_rotation_roll
    params[3] = token_params[3]     # plant_age
    params[4] = 1.0 if abs(1.0 - token_params[4]) < abs(3.0 - token_params[4]) else 3.0 # shoot_type

def get_internode_params(params: np.ndarray, token_params: np.ndarray) -> None:
    params[0] = token_params[5]     # internode_length
    params[1] = token_params[6]     # internode_radius
    params[2] = token_params[7]     # internode_pitch
    params[3] = token_params[8]     # phyllotactic angle

def get_petiole_params(params: np.ndarray, token_params: np.ndarray) -> None:
    params[0] = token_params[9]     # petiole_length
    params[1] = token_params[10]    # petiole radius
    params[2] = token_params[11]    # petiole_pitch
    params[3] = token_params[12]    # petiole_curvature
    params[4] = token_params[13]    # leaflet_scale

def get_leaf_params(params: np.ndarray, token_params: np.ndarray) -> None:
    params[0] = token_params[14]    # leaf_scale
    params[1] = token_params[15]    # leaf pitch
    params[2] = token_params[16]    # leaf yaw
    params[3] = token_params[17]    # leaf roll
    

